def generate_job_descriptions(output_dir):
    import os
    os.makedirs(output_dir, exist_ok=True)
    for jd in JOB_DESCRIPTIONS:
        filepath = os.path.join(output_dir, jd["file"])
        with open(filepath, "w") as f:
            f.write(jd["content"])
    print(f"Generated {len(JOB_DESCRIPTIONS)} job descriptions in {output_dir}/")
    return len(JOB_DESCRIPTIONS)

if __name__ == "__main__":
    count = generate_job_descriptions("job_descriptions")
    print(f"Total JDs: {count}")
